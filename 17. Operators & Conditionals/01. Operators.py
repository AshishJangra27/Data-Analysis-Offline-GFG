# 1. Arithmetic Operators

# Addition
print(2 + 3)
print(2.5 + 3.5)
print(2.5 + 3)
print(True + False)
print('Ashish ' + 'Jangra' + ' is a data science mentor')
print('--------')

# Subtraction
print(2 - 3)
print(2.5 - 3.5)
print(2.5 - 3)
print(False - True)
print('--------')

# Multiplication
print(2 * 3)
print(2.5 * 3.5)
print(2.5 * 3)
print(True * True)
print('--------')

# Division
print(3 / 2)
print(2.5 / 5.0)
print(2.5 / 1)
print('--------')

# Floor Division
print(3 // 2)
print(2.5 // 5.0)
print(2.5 // 1)
print('--------')

# Modulo
print(3 % 2)
print(2.5 % 5.0)
print(2.5 % 1)
print('--------')

# Exponentiation
print(3 ** 5)
print(2.5 ** 5.0)
print(2.5 ** 1)
print('--------')

# abs
print(abs(-3.5))
print(abs(-35))
print('--------')

# round
print(round(3.42349238, 4))
print('--------')

# pow
print(pow(3,5))
print(pow(5,3))
print('--------')

# 2. Comparison Operators

# ==
print(1 == 2)
print(1.0 == 1)
print('A' == 'a')
print(True == 1)
print(False == 0)
print('--------')

# !=
print(1 != 2)
print(1.0 != 1)
print('A' != 'a')
print(True != 1)
print(False != 0)
print('--------')

# >
print(1 > 2)
print(1.0 > 1)
print(3 > True)
print(False > 0)
print('--------')

# <
print(1 < 2)
print(1.0 < 1)
print(3 < True)
print(False < 0)
print('--------')

# <=
print(1 <= 2)
print(1.0 <= 1)
print(3 <= True)
print(False <= 0)
print('--------')

# >=
print(1 >= 2)
print(1.0 >= 1)
print(3 >= True)
print(False >= 0)
print('--------')

# 3. Logical Operators

# and
print(True and True)
print(False and True)
print(True and False)
print(False and False)
print('--------')

# or
print(True or True)
print(False or True)
print(True or False)
print(False or False)
print('--------')

# not
print(not True)
