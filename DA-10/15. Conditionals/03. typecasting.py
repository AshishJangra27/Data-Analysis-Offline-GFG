# typecasting
# int()
print(int(3.5), int(-3.5), int('-1476476'), int(True))


# float()
print(float(3), float(-3), float('-1476476'), float(True))

# str()
print(str(3), str(-3), str(3.5), str(True))

# bool()
print(bool(3), bool(-3), bool(3.5), bool('A'), bool(' '), bool(0), bool(0.0), bool(''))






